<h2 id="">身份验证</h2>
<h3 id="jwt-bearer-token">JWT Bearer Token</h3>
<p>API 使用 <strong>JWT (JSON Web Token)</strong> 进行身份验证。受保护的接口需要在 HTTP Header 中包含：</p>
<pre><code class="language-http">Authorization: Bearer &lt;your-jwt-token&gt;</code></pre>
<blockquote>
<p>[!TIP]</p>
<ol>
<li>您可以通过 <code>api/v1/auth/login</code> 接口获取 Token。</li>
<li>Token 有效期及刷新策略请参考“用户认证”节下的具体说明。</li>
</ol>
</blockquote>
<h3 id="http-header">必须的 HTTP Header</h3>
<p>所有 API 请求均应包含以下头部以确保正确的交互：</p>
<pre><code class="language-http">Content-Type: application/json
Accept: application/json
Authorization: Bearer &lt;token&gt;  # 仅受保护接口需要</code></pre>
