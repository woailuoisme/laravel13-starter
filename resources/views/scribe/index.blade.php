<h2 id="">列表查询规范</h2>
<p>本 API 列表端点支持功能强大的过滤、排序和关联加载功能。</p>
<h3 id="1-filtering">1. 过滤 (Filtering)</h3>
<p>使用 <code>filter[字段名]</code> 语法进行筛选：</p>
<ul>
<li><strong>基本过滤</strong>: <code>GET /users?filter[name]=张三</code></li>
<li><strong>多个过滤</strong>: <code>GET /users?filter[name]=张三&amp;filter[status]=active</code></li>
<li><strong>范围过滤</strong> (如适用): <code>GET /orders?filter[created_at_start]=2024-01-01&amp;filter[created_at_end]=2024-01-31</code></li>
</ul>
<h3 id="2-sorting">2. 排序 (Sorting)</h3>
<p>使用 <code>sort</code> 参数进行排序：</p>
<ul>
<li><strong>升序</strong>: <code>GET /users?sort=id</code></li>
<li><strong>降序</strong>: <code>GET /users?sort=-id</code> (字段名前加 <code>-</code>)</li>
<li><strong>多字段排序</strong>: <code>GET /users?sort=-id,name</code></li>
</ul>
<h3 id="3-inclusion">3. 关联加载 (Inclusion)</h3>
<p>使用 <code>include</code> 参数加载关联资源（如：获取用户时顺便获取他的角色）：</p>
<ul>
<li><strong>加载关联</strong>: <code>GET /users?include=roles,profile</code></li>
</ul>
<h3 id="4-fields-scaling">4. 字段筛选 (Fields Scaling)</h3>
<p>使用 <code>fields[表名]</code> 仅返回指定字段：</p>
<ul>
<li><strong>限定字段</strong>: <code>GET /users?fields[users]=id,nickname,email</code></li>
</ul>
<h3 id="5-pagination">5. 分页 (Pagination)</h3>
<p>列表接口采用标准分页：</p>
<ul>
<li><code>page</code>: 页码（默认: 1）</li>
<li><code>per_page</code>: 每页记录数（默认: 15，最大: 100）</li>
</ul>
<p><strong>综合示例</strong>：
<code>GET /api/v1/users?include=roles&amp;filter[status]=active&amp;sort=-created_at&amp;page=1&amp;per_page=20</code></p>
<h3 id="">分页响应示例</h3>
